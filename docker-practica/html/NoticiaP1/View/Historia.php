<h2>Escuela Municipal Carileufu celebró 77 años de vida como institución</h2>
<br>

<p>
La directora del establecimiento organizó una tertulia educativa para dar a conocer la historia y trayectoria del recinto.

Dpto. Comunicaciones

Municipalidad de Pucón

“Los inicios de la Escuela Municipal de Carileufu, Pucón, se remontan en la década de los 40, con una pequeña casa de campo que llevaba como nombre “Escuela 25 de Liucura”, ubicada pasado el puente del mismo nombre -en un terreno que fue donado por Ignacio Paillalef-, cuyo primer director fue Roselin Paredes”.

Así lo narraron los vecinos del sector en una recreativa tertulia, organizada por la directora del establecimiento Verónica Villegas, quien comentó que “en esta oportunidad quisimos conocer la historia de la escuela, donde se invitó a quienes fueron sus primeros profesores, entre ellos: Germán Barrios y su esposa, Jorge Albornoz, los hermanos Ana y Francisco Colipe Quilaleo, vecinos y docentes”.

El profesor Albornoz, hoy retirado, contó que hizo clases en el año 1964 junto al actual director de la Escuela Municipal Los Arrayanes, Luis Rody Esparza. “No fueron años fáciles, la conexión vial no era buena y la “escuelita” era una infraestructura muy básica. Sin embargo, se trabajó bien y se tenía que hacer clases a niños de primero a sexto básico”.

En los años 70, el recinto fue trasladado al terreno actual, gracias a una donación del matrimonio Ramón Colipe e Isabel Quilaleo, cuyo director fue Germán Barrios Jaramillo. La edificación fue la más moderna de la Región.

Finalmente, la Villegas expresó sus agradecimientos a cada uno de los presentes en la tertulia, en especial a los hijos del matrimonio Colipe–Quilaleo, Francisco y Ana Colipe, los cuales se mostraron gratos al saber que lo donado por sus padres se ha utilizado para fines educativos.
</p>
